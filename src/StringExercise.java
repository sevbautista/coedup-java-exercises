public class StringExercise {
    public static void main(String[] args) {
//        EXERCISE 1. STRING BASICS
        //We don't need no education
        //We don't need no thought control
        String pink = "We don't need no education\n" + "We don't need no thought control\n";
        System.out.println(pink);
        //Check "this" out!, "s inside of "s!
        String esses = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(esses);
        //In windows, the main drive is usually C:\
        String windows = "In windows, the main drive is usually C:\\\n";
        System.out.println(windows);


        //I can do backslashes \, double backslashes \\, and the amazing triple backslash \\\!
        String backslashes = "I can do backslashes \\, double backslashes \\\\,\n" +  "and the amazing triple backslash \\\\\\!\n";
        System.out.println(backslashes);


    }
}
